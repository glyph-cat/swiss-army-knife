import { Sanitize } from '.'

test(Sanitize.toBoolean.name, () => {
  expect(Sanitize.toBoolean(true)).toBeTrue()
  expect(Sanitize.toBoolean('true')).toBeTrue()
  expect(Sanitize.toBoolean('Yes')).toBeTrue()
  expect(Sanitize.toBoolean('YES')).toBeTrue()
  expect(Sanitize.toBoolean('yes')).toBeTrue()
  expect(Sanitize.toBoolean('Y')).toBeTrue()
  expect(Sanitize.toBoolean('y')).toBeTrue()
  expect(Sanitize.toBoolean('1')).toBeTrue()
  expect(Sanitize.toBoolean(1)).toBeTrue()

  expect(Sanitize.toBoolean(false)).toBeFalse()
  expect(Sanitize.toBoolean('false')).toBeFalse()
  expect(Sanitize.toBoolean('No')).toBeFalse()
  expect(Sanitize.toBoolean('NO')).toBeFalse()
  expect(Sanitize.toBoolean('no')).toBeFalse()
  expect(Sanitize.toBoolean('N')).toBeFalse()
  expect(Sanitize.toBoolean('n')).toBeFalse()
  expect(Sanitize.toBoolean('0')).toBeFalse()
  expect(Sanitize.toBoolean(0)).toBeFalse()
  expect(Sanitize.toBoolean(null)).toBeFalse()
  expect(Sanitize.toBoolean(undefined)).toBeFalse()
  expect(Sanitize.toBoolean([])).toBeFalse()
  expect(Sanitize.toBoolean('')).toBeFalse()
})

test(Sanitize.toString.name, () => {
  expect(Sanitize.toString('')).toBe('')
  expect(Sanitize.toString(null)).toBe('null')
  expect(Sanitize.toString(undefined)).toBe('undefined')
  expect(Sanitize.toString(42)).toBe('42')
  expect(Sanitize.toString(' foo bar ')).toBe('foo bar')
  expect(Sanitize.toString({ firstName: 'John' })).toBe('{"firstName":"John"}')
})
